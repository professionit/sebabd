// ===== UTILITY FUNCTIONS =====

let _cachedAllServices: Service[] | null = null;

export function getAllServices(): Service[] {
  if (_cachedAllServices) return _cachedAllServices;
  const all: Service[] = [];
  for (const ministry of ministries) {
    for (const service of ministry.services) {
      all.push({
        ...service,
        ministryId: ministry.id,
        ministryName: ministry.name,
        ministryShort: ministry.short,
        ministryIcon: ministry.icon,
      });
    }
  }
  _cachedAllServices = all;
  return all;
}

export function getAllCategories(): string[] {
  const cats = new Set<string>();
  for (const service of getAllServices()) {
    cats.add(service.category);
  }
  return Array.from(cats);
}

export function getFreeServicesCount(): number {
  return getAllServices().filter(s => s.free).length;
}

export function findServiceById(id: string): Service | undefined {
  return getAllServices().find(s => s.id === id);
}

